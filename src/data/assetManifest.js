export function getAssetRecordById(id) {
  return assetRegistry.find((asset) => asset.id === id) || null;
}

export function isApprovedVisibleAsset(id, usage) {
  const asset = getAssetRecordById(id);
  return Boolean(
    asset
      && asset.sourceType === "raster"
      && asset.approval === "approved"
      && asset.visible === true
      && (!usage || asset.usage === usage)
  );
}


